package fundamentos.operadores;

public class Unários {

	public static void main(String[] args) {
		
		int A = 1;
		
		A++; // Adição efetuada depois (sem pressa)
		A--; // Subtração efetuada depois (sem pressa)
		++A; // Adição efetuada antes (com pressa)
		--A; // Subtração efetuada antes (com pressa)
		
		System.out.println(A);
	    
	}
}
