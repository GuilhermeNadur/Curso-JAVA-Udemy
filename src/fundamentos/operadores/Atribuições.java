package fundamentos.operadores;

public class Atribuições {

	public static void main(String[] args) {
		
		int A = 1;
		int B = 2;
		int C = 3;
		
		C += B; // C = C + B
		C -= A; // C = C - B
		C *= B; // C = C * B
		C /= C; // C = C / B
		C %= 2; // C = C % 2
		C++;    // C = C + 1
		C--;    // C = C - 1
		
	}
}
